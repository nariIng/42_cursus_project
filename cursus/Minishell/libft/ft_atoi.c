int	ft_atoi(const char *nptr)
{
	int		i;
	int		nb;
	int		signe;
	char	*s;

	s = (char *)nptr;
	i = 0;
	nb = 0;
	signe = 1;
	while (s[i] != '\0' && (s[i] == 32 || (s[i] >= 9 && s[i] <= 13)))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			signe *= -1;
		i++;
	}
	while (s[i] != '\0' && s[i] >= '0' && s[i] <= '9')
	{
		nb = nb * 10 + s[i] - '0';
		i++;
	}
	return (nb * signe);
}
